/*
  Object Methods ---> entries, keys, values
*/

const obj = {
    name: "YoYo",
    age: 99,
    city: "Mumbai"
};

const entries = Object.entries(obj);      // entries
console.log(entries);

const keys = Object.keys(obj);            // keys
console.log(keys);

const values = Object.values(obj);        // values
console.log(values);


